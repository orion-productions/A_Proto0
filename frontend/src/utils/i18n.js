// Get translation function
export const t = (key, lang = 'en', params = {}) => {
  const langTranslations = translations[lang] || translations.en;
  let translation = langTranslations[key] || translations.en[key] || key;
  
  // Replace parameters in translation (e.g., {model}, {deviceInfo}, {error})
  Object.keys(params).forEach(param => {
    translation = translation.replace(new RegExp(`\\{${param}\\}`, 'g'), params[param]);
  });
  
  return translation;
};

// Translate tool description
export const translateToolDescription = (description, lang = 'en') => {
  const translationKey = toolDescriptionMap[description];
  if (translationKey) {
    return t(translationKey, lang);
  }
  return description; // Return original if no translation found
};

// Hook for React components
export const useTranslation = (selectedLanguage) => {
  return (key) => t(key, selectedLanguage);
};

